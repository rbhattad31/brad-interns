# if
print("=" * 20)
a = 5
if a > 0:
    print("it is a positive number")
print("=" * 20)
# if_else
n = 5
if n > 0:
    print("it is a positive number")
else:
    print("it is a negative number")
print("=" * 20)
# if_elif_else
p = 5
if p > 0:
    print("it is a positive number")
elif p < 0:
    print("it is a negative number")
else:
    print("zero")
print("=" * 20)
# short_hand_if
x = 5
if x > 0: print("it is a positive number")
print("=" * 20)
# short_hand_if_else
print("it is a positive number") if x > 0 else print("it is a negative number")
print("=" * 20)
